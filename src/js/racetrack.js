function run(horse) {
  return new Promise((resolve, reject) => {
    const time = getRandomTime(2500, 3500);

    setTimeout(() => {
      resolve({ horse, time });
    }, time);
  });
}

function getRandomTime(min, max) {
  return Math.floor(Math.random() * (max - min + 1) + min);
}
